CREATE OR REPLACE PROCEDURE overdue_books
AS
    COUNTER   NUMBER(4) := 0;
    CURSOR LOANCUR IS
    SELECT MEMBER_NO, DUE_DATE, SYSDATE, ISBN, COPY_NO 
   FROM loan
   WHERE due_date < SYSDATE;
BEGIN
  FOR MEMBER IN LOANCUR LOOP
    DBMS_OUTPUT.PUT_LINE('MEMBER NO IS '||MEMBER.MEMBER_NO||' DUE DATE '
                                        ||MEMBER.DUE_DATE);
   COUNTER := COUNTER + 1;
   EXIT WHEN COUNTER > 50;
  END LOOP;
END OVERDUE_BOOKS;
