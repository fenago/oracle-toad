CREATE TABLE CUSTOMER (
 CUSTID              NUMBER (6) NOT NULL,
 NAME                VARCHAR2 (45),
 ADDRESS             VARCHAR2 (40),
 CITY                VARCHAR2 (30),
 STATE               VARCHAR2 (2),
 ZIP                 VARCHAR2 (9),
 AREA                NUMBER (3),
 PHONE               VARCHAR2 (9),
 REPID               NUMBER (4) NOT NULL,
 CREDITLIMIT         NUMBER (9,2),
