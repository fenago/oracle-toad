-- CREATE BIG TABLESPACE TSBIGXX IN DATABASE CLASSDBX
	CREATE TABLESPACE TSBIGXX
	IN CLASSDBX
	LOCKSIZE ANY
	CLOSE NO
	USING VCAT YYYY
--	USING STOGROUP (DSNSTG02)
	FREEPAGE 10
	PCTFREE 10;

	COMMIT;

-- CREATE BIG TABLE
      CREATE TABLE BIG
     (BIGNO                      INTEGER  NOT NULL,
      BNAME                      CHAR(50) NOT NULL
      )
      IN CLASSDBX.TSBIGXX
      ;

   COMMIT;

-- CREATE NON-UNIQUE INDEXES ON BIGNO AND BIGNAME

	CREATE INDEX BIGX1
	ON BIG (BIGNO);
	
   COMMIT;

	CREATE INDEX BIGX2
	ON BIG (BNAME);

   COMMIT;

     

